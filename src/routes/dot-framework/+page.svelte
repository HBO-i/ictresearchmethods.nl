<h1>The DOT Framework</h1>
<section>
	<h1>Research in ICT</h1>
	<p>
		As a bachelor of ICT, your research aims at creating an ICT product which fits its needs (in
		this context an ICT product can be a software product, or an ICT design or advise/report). The
		research is focused on the product, and not per se on creating new knowledge which is the aim in
		most scientific research.
	</p>
	<p>To give you a glimpse of possible ICT research:</p>
	<ul>
		<li>investigating the stakeholders' wishes</li>
		<li>choosing the most appropriate technology for a (part of) ICT system</li>
		<li>investigating the usefulness of a certain technology or framework</li>
		<li>testing the quality of an ICT product</li>
	</ul>
</section>

<section>
	<h1>The Development Oriented Triangulation Framework</h1>
	<p>
		To help you in your ICT research, we advise the use of the <i
			>Development Oriented Triangulation</i
		>
		(DOT) framework (<a
			rel="nofollow"
			href="https://www.researchgate.net/publication/266684802_Design_Patterns_for_Mixed-Method_Research_in_HCI"
			target="_blank">[1]</a
		>). The DOT framework can help you to structure your research and to communicate about it. The
		Development Oriented Triangulation (DOT) framework consists of three levels:
	</p>
	<ul>
		<li>The "What" of your research (the domains)</li>
		<li>The "Why"of your research (the trade-offs)</li>
		<li>The "How" of your research (the strategies and methods)</li>
	</ul>
</section>

<section>
	<h1>
		<span>The "What"of your research (the domains)</span>
	</h1>
	<p>
		You can research both the specific application context of your project and the more general
		knowledge available that can help your project. Both types of research will help you, and are
		necessary to create your own "innovation", your new product/advice/report. We can express this
		by three domains. The first domain is the "application domain". This is the domain of the
		specific context that the ICT project takes place. The second domain is of "available work". All
		available theory, models and other artefacts that you can use are part of the available work
		domain. Thirdly is the "innovation domain", where your actual innovation takes place and all of
		the research is done.
	</p>
</section>

<section>
	<h1>
		<span>The "Why" of your research</span>
	</h1>
	<p>
		Explaining what you want to obtain with your research will help you to better structure your
		research. If you do research in the application context your reason for research will probably
		be to obtain a product which is relevant for the stakeholders. You are trying to optimise the <b
			>fit</b
		>
		between your product and the application context. Oftentimes you want to assure that your product
		is up to contemporary quality standards. In those cases you want to use all the <b>expertise</b>
		available to create your product. You do this by doing research in the "available work" domain. As
		there are few methods that simultaneously optimise fit and expertise it is often needed to combine
		both types of methods in the project.
	</p>
	<p>
		Similarly there is a tradeoff between optimising <b>overview</b> and <b>certainty</b>. Often,
		mostly in the beginning you want to gain a good overview over what is needed or what is
		available. At other times you want to test specific aspects of your work, making sure it works.
		In these cases you try to configure your research to optimise "certainty" about your hypotheses
		or goals.
	</p>
	<p>
		Lastly, in most cases your research might require a <b>data</b> oriented approach to justify
		your choices, prove completeness and so on. Sometimes, however, it is a good idea to choose a
		more
		<b>inspiration</b>
		based method, like the <a href="/workshop/brainstorm" title="Brainstorm">Brainstorm</a> method.
	</p>
</section>

<section class="strategies-methods">
	<h1>The "How" of your research (the strategies and methods)</h1>
	<p>
		So during a project you try to learn as much as you can about the context of available work, the
		application context and the innovation space. What are ways of learning these things? The
		DOT-Framework has 5 research strategies.
	</p>

	<h2><a href="/library">Library</a></h2>
	<q>Standing on the shoulds of giants.</q>
	<p>
		Library research is done to explore what is already done and what guidelines and theories exist
		that could help you further your design. Since the advent of the internet library research is
		also called desk research.
	</p>

	<h2><a href="/field">Field</a></h2>
	<q>Understand your users.</q>
	<p>
		Field research is done to explore the application context. You apply a field strategy to get to
		know your end users, their needs, desires and limitations as organizational and physical
		contexts in which they will use your product.
	</p>

	<h2><a href="/lab">Lab</a></h2>
	<q>To measure is to know.</q>
	<p>
		Lab research is done to test parts or concepts of your product, of the final product. You use
		lab research to learn if things work out the way you intended them, or to test different
		scenarios.
	</p>

	<h2><a href="/showroom">Showroom</a></h2>
	<q>Know & show your contribution.</q>
	<p>
		Showroom research is done to test your ideas in relation to existing work. Showing your
		prototype to experts can be a form of showroom research or spelling out how your product is
		different from the competition. Also testing your product to general guidelines is a form of
		showroom research.
	</p>

	<h2><a href="/workshop">Workshop</a></h2>
	<q>Seek variation and improvement!</q>
	<p>
		Workshop research is done to explore opportunities. Prototyping, designing and co-creation
		activities are all ways to gain insights in what is possible and how things could work.
	</p>
</section>

<section>
	<h1>Triangulation</h1>
	<p>
		Most projects require a mix of methods to get answers to your research questions. In most cases,
		both "fit" and "expertise" are of importance, and the same holds for "overview" and "certainty".
		Therefore, combining the appropriate methods to cover for the different views is important.
		Combining methods with opposing goals is sometimes called "method triangulation". Some of the
		more common combinations of methods have been defined as design patterns.
	</p>
</section>

